package controller;

/**
 *
 * @author macedo
 */
public class Point4P {
    public double x;
    public double y;
    public double z;
    public double k;

    public Point4P(double x, double y, double z, double k) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.k = k;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public double getZ() {
        return z;
    }

    public double getK() {
        return k;
    }
    
}
